#!/usr/bin/env node
// build-figma-plugin — bake the design tokens from app/globals.css into the
// Token Sync Figma plugin (figma-token-sync/code.js).
//
// A Figma plugin runs inside Figma and cannot read the local filesystem, so the
// token values can't be read at runtime — they are embedded here at build time.
// This reads the :root (light) and .dark blocks, converts each color token to an
// { r, g, b, a } value (0..1, the shape Figma's setValueForMode expects), and
// writes the map into code.js between the TOKENS markers.
//
// Usage:  npm run figma:plugin:build

import { readFileSync, writeFileSync } from "node:fs"
import { join } from "node:path"

const root = process.cwd()
const CSS = join(root, "app/globals.css")
const PLUGIN = join(root, "figma-token-sync/code.js")

/* --------------------------------------------------------------- color parse */

function hslToRgba(h, s, l, a) {
  s /= 100
  l /= 100
  const k = (n) => (n + h / 30) % 12
  const f = (n) => l - s * Math.min(l, 1 - l) * Math.max(-1, Math.min(k(n) - 3, 9 - k(n), 1))
  return { r: round(f(0)), g: round(f(8)), b: round(f(4)), a }
}

function hexToRgba(hex) {
  const h = hex.replace("#", "")
  const n = (i) => parseInt(h.slice(i, i + 2), 16) / 255
  return { r: round(n(0)), g: round(n(2)), b: round(n(4)), a: h.length >= 8 ? round(n(6)) : 1 }
}

const round = (x) => Math.round(x * 1e4) / 1e4
const norm = (s) => s.toLowerCase().replace(/[^a-z0-9]/g, "")

// "hsl(48 33% 97%)" | "hsl(0 0% 0% / 30%)" | "#rrggbb[aa]" -> { r,g,b,a } or null
function parseColor(value) {
  const v = value.trim()
  let m
  if ((m = v.match(/^#([0-9a-fA-F]{6}([0-9a-fA-F]{2})?)$/))) return hexToRgba(v)
  if ((m = v.match(/hsl\(\s*([\d.]+)\s+([\d.]+)%\s+([\d.]+)%\s*(?:\/\s*([\d.]+)%?)?\s*\)/))) {
    const a = m[4] === undefined ? 1 : (v.includes("%", v.indexOf("/")) ? +m[4] / 100 : +m[4])
    return hslToRgba(+m[1], +m[2], +m[3], round(a))
  }
  return null // alias / non-color (e.g. --radius) -> skip
}

/* ------------------------------------------------- read tokens from globals.css */

function blockBody(css, selector) {
  // selector blocks in this file have no nested braces
  const re = new RegExp(`(?:^|\\n)${selector}\\s*\\{([^}]*)\\}`, "m")
  return css.match(re)?.[1] ?? ""
}

function tokensFor(css, selector) {
  const out = {}
  for (const m of blockBody(css, selector).matchAll(/--([\w-]+):\s*([^;]+);/g)) {
    const color = parseColor(m[2])
    if (color) out[norm(m[1])] = color
  }
  return out
}

/* ---------------------------------------------------------------------- main */

const css = readFileSync(CSS, "utf8")
const light = tokensFor(css, ":root")
const dark = tokensFor(css, "\\.dark")

const map = {}
for (const key of new Set([...Object.keys(light), ...Object.keys(dark)])) {
  map[key] = {}
  if (light[key]) map[key].light = light[key]
  if (dark[key]) map[key].dark = dark[key]
}

const json = JSON.stringify(map, null, 2).replace(/\n/g, "\n")
const block =
  `// >>> TOKENS (generated by scripts/build-figma-plugin.mjs from app/globals.css — do not edit by hand) >>>\n` +
  `const TOKENS = ${json};\n` +
  `// <<< TOKENS <<<`

const code = readFileSync(PLUGIN, "utf8")
const next = code.replace(
  /\/\/ >>> TOKENS[\s\S]*?\/\/ <<< TOKENS <<</,
  block,
)
if (next === code && !/\/\/ >>> TOKENS/.test(code)) {
  console.error("✗ Could not find the TOKENS markers in figma-token-sync/code.js")
  process.exit(1)
}
writeFileSync(PLUGIN, next)

console.log(
  `✓ baked ${Object.keys(map).length} color tokens into figma-token-sync/code.js ` +
    `(${Object.keys(light).length} light, ${Object.keys(dark).length} dark)`,
)
